char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
    int i = 0;
    int j = 0;
    while(dest[i] != '\0')
     i++;
     while(j < n)
     {
         dest[i] = src[j];
         i++;
         j++;
     }
     dest[i] = '\0';
}
